    function showExams(subject) {
      const examList = document.getElementById("examList");
      const examSection = document.getElementById("examSection");
      const subjectTitle = document.getElementById("subjectTitle");

      subjectTitle.textContent = `الامتحانات: ${subject}`;
      examList.innerHTML = "";

      const exams = examsData[subject] || [];

      if (exams.length === 0) {
        examList.innerHTML = `<p style='color:#eee'>لا توجد امتحانات متوفرة حالياً لهذه المادة. سيتم إضافتها قريباً.</p>`;
      } else {
        exams.forEach(exam => {
          const item = document.createElement("div");
          item.className = "exam-item";
          item.innerHTML = `
            <span>${exam.name}</span>
            <a href="${exam.url}" download>تحميل</a>
          `;
          examList.appendChild(item);
        });
      }

      examSection.style.display = "block";
      examSection.scrollIntoView({ behavior: 'smooth' });
    }
  